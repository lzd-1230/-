#include <stdio.h>
#include <stdlib.h>

//if版本 
int main() {
	float I,wage;
	if(I<=100000){
		wage=I*0.1;
	} else if(100000<I<=200000){
		wage=100000*0.1+0.075*(I-100000);
	}else if(200000<I<=400000){
		wage=100000*0.1+0.075*(I-100000)+0.05*(I-200000);
	}else if(4000000<I<=600000){
		wage=100000*0.1+0.075*(I-100000)+0.05*(I-200000)+(I-400000)*0.03;
	}else if(600000<I<=1000000){
		wage=100000*0.1+0.075*(I-100000)+0.05*(I-200000)+(I-400000)*0.03+0.015*(I-600000);
	}else{
		wage=100000*0.1+0.075*(I-100000)+0.05*(I-200000)+(I-400000)*0.03+0.015*(I-600000)+0.01(I-1000000);
	}
	printf("您的奖金为%f",wage);
	return 0;
}
